#!/usr/bin/env node
/**
 * Generate the e2e/README.md test inventory section from Playwright's
 * `--list --reporter=json` output.
 *
 * Tests are grouped by their `@area:<tag>` annotation (extracted from the
 * test title by Playwright into the `tags` array). Tests without an
 * `@area:` tag land under "uncategorized" so they're still visible.
 *
 * Output is spliced between `<!-- BEGIN:E2E-INVENTORY -->` and
 * `<!-- END:E2E-INVENTORY -->` markers in e2e/README.md.
 *
 * CI runs this and fails on git diff to keep the inventory in sync with
 * the actual suite.
 */
import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const README_PATH = path.join(REPO_ROOT, 'e2e', 'README.md');
const BEGIN_MARKER = '<!-- BEGIN:E2E-INVENTORY -->';
const END_MARKER = '<!-- END:E2E-INVENTORY -->';
const PLAYWRIGHT_CLI = path.join(REPO_ROOT, 'node_modules', 'playwright', 'cli.js');

function listTests() {
  const json = execFileSync(
    process.execPath,
    [PLAYWRIGHT_CLI, 'test', '--list', '--reporter=json', '--config=e2e/playwright.config.ts'],
    { cwd: REPO_ROOT, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 },
  );
  return JSON.parse(json);
}

/**
 * Walk the recursive suites tree and yield every spec with its full path.
 * Each yielded entry: { title, file, line, tags, describePath }
 */
function* walkSpecs(suites, describePath = []) {
  for (const suite of suites ?? []) {
    const isFileSuite = suite.line === 0;
    const nextDescribe = isFileSuite ? describePath : [...describePath, suite.title];
    for (const spec of suite.specs ?? []) {
      yield {
        title: spec.title,
        file: spec.file,
        line: spec.line,
        tags: spec.tags ?? [],
        describePath: nextDescribe,
      };
    }
    if (suite.suites) {
      yield* walkSpecs(suite.suites, nextDescribe);
    }
  }
}

function extractArea(tags) {
  // Playwright strips the leading '@' when extracting tags from the title:
  // `test('foo @area:spawn')` -> tags: ['area:spawn']. Accept either form
  // so the generator survives upstream Playwright behavior changes.
  for (const tag of tags) {
    const match = /^@?area:(.+)$/.exec(tag);
    if (match) return match[1];
  }
  return null;
}

function groupByArea(specs) {
  const groups = new Map();
  for (const spec of specs) {
    const area = extractArea(spec.tags) ?? 'uncategorized';
    if (!groups.has(area)) groups.set(area, []);
    groups.get(area).push(spec);
  }
  // Sort areas: known ones in a defined order, then alphabetical, "uncategorized" last
  const KNOWN_ORDER = ['spawn', 'lifecycle', 'cross-cutting', 'teams', 'matrix', 'standalone'];
  const sorted = new Map();
  for (const known of KNOWN_ORDER) {
    if (groups.has(known)) sorted.set(known, groups.get(known));
  }
  for (const area of [...groups.keys()].sort()) {
    if (!sorted.has(area) && area !== 'uncategorized') sorted.set(area, groups.get(area));
  }
  if (groups.has('uncategorized')) sorted.set('uncategorized', groups.get('uncategorized'));
  // Stable inner sort by file:line
  for (const list of sorted.values()) {
    list.sort((a, b) => (a.file !== b.file ? a.file.localeCompare(b.file) : a.line - b.line));
  }
  return sorted;
}

function renderInventory(groups) {
  const lines = [];
  const total = [...groups.values()].reduce((acc, list) => acc + list.length, 0);
  // Plain text, no markdown emphasis, so Prettier can't rewrite our output
  // and tests with intraword underscores (e.g. `run_in_background`) don't
  // accidentally pair with the trailing italics marker.
  lines.push(
    `${total} tests total. Generated by \`scripts/generate-e2e-inventory.mjs\`. Re-run after adding or removing tests.`,
  );
  lines.push('');
  for (const [area, list] of groups) {
    lines.push(`### \`@area:${area}\` (${list.length} tests)`);
    lines.push('');
    for (const spec of list) {
      const titleClean = spec.title.replace(/\s*@area:\S+/g, '').trim();
      const describe = spec.describePath.length > 0 ? ` (${spec.describePath.join(' › ')})` : '';
      lines.push(`- \`e2e/${spec.file}:${spec.line}\` — ${titleClean}${describe}`);
    }
    lines.push('');
  }
  return lines.join('\n');
}

function splice(readme, payload) {
  const beginIdx = readme.indexOf(BEGIN_MARKER);
  const endIdx = readme.indexOf(END_MARKER);
  if (beginIdx === -1 || endIdx === -1) {
    throw new Error(
      `e2e/README.md must contain ${BEGIN_MARKER} and ${END_MARKER} markers. Add them and re-run.`,
    );
  }
  const before = readme.slice(0, beginIdx + BEGIN_MARKER.length);
  const after = readme.slice(endIdx);
  return `${before}\n\n${payload}\n${after}`;
}

function main() {
  const result = listTests();
  const specs = [...walkSpecs(result.suites)];
  const groups = groupByArea(specs);
  const payload = renderInventory(groups);
  const readme = readFileSync(README_PATH, 'utf8');
  const next = splice(readme, payload);
  if (next === readme) {
    console.log('e2e/README.md inventory unchanged.');
    return;
  }
  writeFileSync(README_PATH, next, 'utf8');
  console.log(
    `e2e/README.md inventory updated (${specs.length} tests across ${groups.size} areas).`,
  );
}

try {
  main();
} catch (err) {
  console.error(err instanceof Error ? err.message : String(err));
  process.exit(1);
}
